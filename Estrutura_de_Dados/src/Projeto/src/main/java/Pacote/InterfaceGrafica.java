    private void btnMulherMaiorAlturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMulherMaiorAlturaActionPerformed
        AreaTexto.setText("");
        if (mulheres.isEmpty()) {
            AreaTexto.setText("Pilha encontra-se vazia!");
            return;
        }
        String info = "";
        double maxAltura = Double.MIN_VALUE;
        for(Atleta a : mulheres) {
            double altura = a.getAltura();
            if (altura > maxAltura) {
                maxAltura = altura;
                info = "Nome: " + a.getNome() + ", País: " + a.getPais() + ", Sexo: " + a.getSexo() + ", Altura: " + a.getAltura() + "m\n";
            }
        }
        AreaTexto.setText("A atleta que obteve a maior altura na competição salto em altura: \n" + info);
    }//GEN-LAST:event_btnMulherMaiorAlturaActionPerformed

    private void btnMediaGeralActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMediaGeralActionPerformed
        AreaTexto.setText("");
        if (listaGeral.isEmpty()) {
            AreaTexto.setText("Lista encontra-se vazia!");
            return;
        }
        int aux = 0;
        double acumulador = 0, mediaGeral = 0;
        for(Atleta a : listaGeral) {
            acumulador += a.getAltura();
            aux++;
        }
        mediaGeral = acumulador / aux;
        AreaTexto.setText("Média geral obtida na competição foi de: " + mediaGeral + "m");
    }//GEN-LAST:event_btnMediaGeralActionPerformed

    private void btnMediaMasculinaBrasileiraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMediaMasculinaBrasileiraActionPerformed
        AreaTexto.setText("");
        if (homens.isEmpty()) {
            AreaTexto.setText("Fila encontra-se vazia!");
            return;
        }
        int aux = 0;
        double mediaBrasileiros = 0, acumulador = 0;
        for(Atleta a : homens) {
            if (a.getSexo().equalsIgnoreCase("Masculino") && a.getPais().equalsIgnoreCase("Brasil")) {
                acumulador += a.getAltura();
                aux++;
            }
        }
        
        if (aux == 0) {
            AreaTexto.setText("Nenhum brasileiro foi encontrado!");
            return;
        }
        
        mediaBrasileiros = acumulador / aux;
        AreaTexto.setText("A média obtida pelos atletas brasileiros foi de: " + mediaBrasileiros + "m");
    }//GEN-LAST:event_btnMediaMasculinaBrasileiraActionPerformed

