    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        AreaTexto.setText("");
        String nome = inputNome.getText();
        String pais = inputPais.getText();
        String sexo;
        if (Masculino.isSelected()) {
            sexo = "Masculino";
        } else {
            sexo = "Feminino";
        }
        String altura = inputAltura.getText();
        if (nome.isEmpty() || pais.isEmpty() || sexo.isEmpty() || altura.isEmpty()) {
            AreaTexto.setText("Algum campo está vazio!");
            return;
        }
        double alturaConvertida = Double.parseDouble(altura);
        if (sexo.equalsIgnoreCase("Feminino")) {
            mulheres.add(new Atleta(nome, pais, sexo, alturaConvertida));
        } else if (sexo.equalsIgnoreCase("Masculino")) {
            homens.add(new Atleta(nome, pais, sexo, alturaConvertida));
        }
        listaGeral.add(new Atleta(nome, pais, sexo, alturaConvertida));
        AreaTexto.setText("Atleta Cadastrado");
    }//GEN-LAST:event_btnCadastrarActionPerformed

